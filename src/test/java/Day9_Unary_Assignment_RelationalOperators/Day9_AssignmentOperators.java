package Day9_Unary_Assignment_RelationalOperators;

public class Day9_AssignmentOperators {

    public static void main(String[] args) {

        int i = 100;

        //System.out.println(i + 1);
        System.out.println("i = " + i);

        i+=1; // i = i + 1
        System.out.println("i = " + i);

        i-=1; // i = i - 1
        System.out.println("i = " + i);

        i=2; // i = i 1
        System.out.println("i = " + i);

        i/=2; // i = i / 2
        System.out.println("i = " + i);

        i%=5; // i = i % 5
        System.out.println("i = " + i);

    }
}
