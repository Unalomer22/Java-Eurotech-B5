package odevler;

public class odev4 {
    public static void main(String[] args) {
        int i = 100;
        System.out.println(i + 1);
        System.out.println(" i = " + 1);

        i+=1 ; // i = i + 1
        System.out.println("i = " + i);

        i-= 1 ; // i = i - 1
        System.out.println("i = " + i);

        i*=2 ;// i = i * 2
        System.out.println("i = " + i);

        i/= 2 ;// i = i / 2
        System.out.println("i = " + i);

        i%= 5 ;// i = i % 5
        System.out.println("i = " + i);
        System.out.println("i = " + i);
    }
}
