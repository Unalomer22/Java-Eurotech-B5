package Day19_WhileLoop;

public class WhileLoop_01 {
    public static void main(String[] args) {

        System.out.println(" While Loop .....");
        int count = 0;
        while ( count < 10) {
            System.out.println( count + " While Welcome to Java");
            count++;
        }

        System.out.println("*************************");

        for ( int i=0 ; i<10 ; i= i+2){
            System.out.println( i + " For Welcome to Java");
        }



    }
}
