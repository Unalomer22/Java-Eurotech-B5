package Day58_Rewiew;

public class Oda {
    private String neOdasi;
    private String renk;

    public Oda(String neOdasi, String renk) {
        this.neOdasi = neOdasi;
        this.renk = renk;
    }

    public String getNeOdasi() {
        return neOdasi;
    }

    public String getRenk() {
        return renk;
    }
}
